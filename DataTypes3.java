class ScientificCalculations {
    public static void main(String[] args) {
        double pi = Math.PI;      // Value of pi
        int triangleAngle = 180;  // Total degrees in a triangle
        int circleAngle = 360;    // Total degrees in a circle

        System.out.println("Value of Pi: " + pi);
        System.out.println("Total angle of a triangle: " + triangleAngle + " degrees");
        System.out.println("Highest degree of a circle: " + circleAngle + " degrees");
    }
}

