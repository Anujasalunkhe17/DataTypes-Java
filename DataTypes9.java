//write a program to print the temperature of the air conditioner in degree and also print standard room temperature using appropriate data types
 class TemperatureDetails {
    public static void main(String[] args) {
        double acTemperature = 22.5;        // Air conditioner temperature in degrees Celsius (double)
        double roomTemperature = 25.0;      // Standard room temperature in degrees Celsius (double)

        System.out.println("Air Conditioner Temperature: " + acTemperature + "°C");
        System.out.println("Standard Room Temperature: " + roomTemperature + "°C");
    }
}