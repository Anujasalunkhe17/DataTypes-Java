//write a program to print the value of gravity and the print the letter used to represent the acceleration due to gravity
 class GravityDetails {
    public static void main(String[] args) {
        double gravity = 9.8;  // Value of gravity in m/s^2 (double)
        char gravitySymbol = 'g';  // Letter used to represent acceleration due to gravity (char)

        System.out.println("Value of Gravity: " + gravity + " m/s^2");
        System.out.println("Symbol for Acceleration due to Gravity: " + gravitySymbol);
    }
}
